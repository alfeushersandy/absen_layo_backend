     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if(Auth::user()->hasRole('super-admin') || Auth::user()->hasRole('admin')){
            $check = CheckClock::latest()->paginate(10)->withQueryString();
        }else{
            $check = CheckClock::whereHas('karyawan', function($q){
                $q->where('nama', Auth::user()->karyawan->nama);
            })->latest()->paginate(10)->withQueryString();
        }
        return view('admin.absen.index', compact('check'));
    }

    public function import(Request $request)
    {
        $this->validate($request, [
            'file' => 'required|mimes:csv,xls,xlsx'
        ]);

        $file = $request->file('file');

        // membuat nama file unik
        $nama_file = $file->hashName();

        //temporary file
        $path = $file->storeAs('public/excel/',$nama_file);

        // import data
        $import = Excel::import(new CheckClockImport(), storage_path('app/public/excel/'.$nama_file));

        //remove from server
        Storage::delete($path);

        if($import) {
            //redirect
            return redirect()->route('admin.check.index')->with(['success' => 'Data Berhasil Diimport!']);
        } else {
            //redirect
            return redirect()->route('admin.check.index')->with(['error' => 'Data Gagal Diimport!']);
        }
    }

